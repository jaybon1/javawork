package simple;


public class Var {

	// 변수는 자료를 담는 집이다

	public static void main(String[] args) {

		// 기본 자료형 변수는 int, char 등의 자료를 담는다
		
		String aa; //선언
		aa = "1"; // 초기화
		aa = "2"; // 변수값 변경

		int 숫자집 = 1;

		char 글자집 = '가';
	

		
		
		
		// 참조 자료형 변수 String, ArrayList, 클래스타입, Integer, Character

		// 레퍼런스 변수

		// 참조 자료형 변수는 주소를 담는다. 주소를 찾아가면 집에 갈수 있다

		String 중앙로7 = "집안의 물건";

		
		
		
		
		System.out.println(숫자집);

		System.out.println(글자집);

		System.out.println(중앙로7);

	}

}
