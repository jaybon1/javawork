package ch01;

public class OperEx01 {

	public static void main(String[] args) {

		// 한 라인에 두가지 변수를 선언 할 수 있다.

		int n1, n2;
		n1 = 5;
		n2 = 3;

		// 인텔리제이 키맵이라면 shift + f10으로 실행

		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 / n2);
		System.out.println(n1 * n2);
		System.out.println(n1 % n2);

	}

}
