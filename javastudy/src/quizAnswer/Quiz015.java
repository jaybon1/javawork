// 3600초는 1시간
// 60초는 1분

// 지금부터 34567초 뒤에 퇴근하려고 한다.
// 몇시간 몇분 뒤에 퇴근하는지 출력하세요.
// 출력예시 : 0시간 0분 0초 뒤에 퇴근합니다.

public class Quiz015 {
	public static void main(String[] args) {
		
		// 정답
		int a = 34567;
		int hour = 0;
		int minute = 0;
		int second = 0;
		
		hour = a/3600;
		
		a = a % 3600;
		
		minute = a/60;
		
		a = a % 60;
		
		second = a;
		
		System.out.println(hour+"시간 "+minute+"분 "+second+"초 뒤에 퇴근합니다.");
		
	}
}
