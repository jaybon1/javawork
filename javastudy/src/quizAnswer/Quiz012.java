package quizAnswer;

// > >= == !=
// 비교 연산자는 true 또는 false 를 리턴한다 
// (2 > 1)은 true  (1 > 2)는 false

// 비교연산자로 모두 다르게 만들어서
// true false true false true false 를 출력해보자

public class Quiz012 {
	public static void main(String[] args) {
		
		int a = 100;
		
		// 정답 (아무렇게나 만들면됨)
		System.out.println(2>1);
		System.out.println(2>3);
		System.out.println(50>=48);
		System.out.println(50>50);
		System.out.println(a > 50);
		System.out.println(a < 50);
		
	}
}
